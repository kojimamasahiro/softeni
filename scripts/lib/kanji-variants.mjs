const TABLE = new Map(Object.entries(KANJI_VARIANTS));

/** 文字列中の異体字を常用字体へ畳む（比較キー生成用）。 */
export function foldKanjiVariants(s) {
  if (!s) return s;
  let out = '';
  for (const ch of s) out += TABLE.get(ch) ?? ch;
  return out;
}
