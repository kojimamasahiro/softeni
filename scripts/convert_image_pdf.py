lines = [line.strip() for line in text.splitlines() if line.strip()]

pairs = []
pair_id = 1

def parse_line(line):
    parts = line.split()
    if len(parts) < 2:
        return None
    last_name = parts[0]
    first_name = parts[1]
    prefecture = parts[2] if len(parts) >= 3 else None
    team = parts[3] if len(parts) >= 4 else None
    return {"lastName": last_name, "firstName": first_name, "prefecture": prefecture, "team": team}

i = 0
while i < len(lines):
    p1 = parse_line(lines[i]); i += 1
    if p1 is None: continue

    if i >= len(lines): break
    p2 = parse_line(lines[i]); i += 1
    if p2 is None: continue

    # team / prefecture を補完
    if p1["team"] and not p2["team"]:
        p2["team"] = p1["team"]
    if p1["prefecture"] and not p2["prefecture"]:
        p2["prefecture"] = p1["prefecture"]

    if p2["team"] and not p1["team"]:
        p1["team"] = p2["team"]
    if p2["prefecture"] and not p1["prefecture"]:
        p1["prefecture"] = p2["prefecture"]

    # tempId
    for p in (p1, p2):
        p["playerId"] = None
        p["tempId"] = f"{p['lastName']}_{p['firstName']}_{p['team']}" if p['team'] else f"{p['lastName']}_{p['firstName']}"

    team_name = p1["team"] if p1["team"] == p2["team"] else "混合"
    pair_name = f"{p1['lastName']}・{p2['lastName']}（{team_name}）"

    pair_obj = {
        "id": pair_id,
        "name": pair_name,
        "information": [p1, p2],
        "category": "doubles"
    }
    pairs.append(pair_obj)
    pair_id += 1

print("[")
for idx, p in enumerate(pairs):
    comma = "," if idx < len(pairs) - 1 else ""  # 最後だけカンマなし
    print(json.dumps(p, ensure_ascii=False, separators=(',', ': ')) + comma)
print("]")
